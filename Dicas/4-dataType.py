# 4. Variáveis e Tipos de Dados
# As variáveis armazenam valores, como a vida do jogador, o ataque, e os itens.

# Exemplo:
jogador_vida = 100  # Inteiro
jogador_nome = "Aldar"  # String
itens = ["Poção de Cura", "Espada de Cristal"]  # Lista

print(f"O herói {jogador_nome} tem {jogador_vida} de vida.")
print(f"Itens no inventário: {itens}")
# Explicação:
# Usamos diferentes tipos de dados: números inteiros para vida, strings para nomes, e listas para armazenar os itens. Isso ajuda a gerenciar os elementos do jogo.