# 1. Estruturas Condicionais (if, elif, else)
# As estruturas condicionais permitem ao programa tomar decisões baseadas em condições.

# Exemplo:
vida = 100
inimigo_presente = True

if inimigo_presente:
    print("Você encontrou um inimigo!")
else:
    print("Tudo está tranquilo.")

# Explicação:
# Se inimigo_presente for True, o jogador recebe uma mensagem de encontro com o inimigo. Caso contrário, a resposta será "Tudo está tranquilo." Isso é essencial para tomar decisões com base no estado do jogo.