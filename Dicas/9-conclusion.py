# Conclusão
# Esses exemplos cobrem os principais conceitos necessários para criar um RPG baseado em escolhas no terminal usando Python. Com esse conhecimento, você pode começar a construir seu jogo adicionando novos personagens, eventos, e desafios. Aqui está um resumo do que aprendemos:

# Condicionais (if/else): Para definir ramificações na história.
# Loops (while): Para criar sistemas de batalha e repetição.
# Funções: Para modularizar o código e criar ações como atacar e usar itens.
# Entrada/Saída (input/print): Para interagir com o jogador.
# Listas/Dicionários: Para armazenar inventário e atributos de personagens.
# Sistemas de Batalha: Combinações de atributos como vida e ataque para criar combates dinâmicos.
# Tratamento de Erros: Garantir que o jogo lide com entradas incorretas sem quebrar.
# Com esses blocos de construção, você está pronto para criar um RPG mais robusto e divertido!